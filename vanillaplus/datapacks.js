  function showInfo(title, info, url) {
    Swal.fire({
      title: title,
      text: info,
      icon: 'info',
      showCancelButton: true,
      confirmButtonColor: '#3085d6',
      cancelButtonColor: '#848484',
      confirmButtonText: 'Download',
    }).then((result) => {
      if (result.isConfirmed) {
        Swal.fire(
          'Downloaded ' + title + '!',
          'The pack has been downloaded. If the download does not start, click <a href="' + url + '">here</a>.',
          'success',
        )
        window.location.href
          = url;
      }
    })
  }
